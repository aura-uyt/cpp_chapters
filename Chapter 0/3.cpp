#include<iostream>
using namespace std;

int main()
{
    int a,b;
    cout<<"Enter the value of a:\n";       //  "<<" is called INSERTION OPERATOR, it DISPLAYS the value
    cin>>a;                                //  ">>" is called EXTRACTION OPERATOR, it TAKES in the VALUES                 

    cout<<"Enter the value of b:\n";
    cin>>b;

    cout<<"The sum is: "<<a+b;
    cout<<"\n";
    return 0;
}
